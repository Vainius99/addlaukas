    public function create(Request $request)
    {

        $clientCount = $request->clientCount;

        if(!$clientCount) {
            $clientCount = 1;
        }

        if($request->clientAdd == "add") {
            $clientCount++;
        } else if ($request->clientAdd == "remove") {
            $clientCount--;
        }

        return view("client.create", ["clientCount" => $clientCount]);
    }

